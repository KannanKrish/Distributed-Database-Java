    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            if (!jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString().equals("")) {
                String studentID = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
                EntityManagerFactory emf = javax.persistence.Persistence.createEntityManagerFactory("DBApplicationPU");
                EntityManager em = emf.createEntityManager();
                em.getTransaction().begin();
                Fee fee = (Fee) em.createNamedQuery("Fee.findByStudentID").setParameter("studentID", Integer.parseInt(studentID)).getSingleResult();
                em.remove(fee);
                em.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "The Fee is deleted from Database.", "Warning", 1);
                refreshTable();
            } else {
                JOptionPane.showMessageDialog(null, "Must select deleteable row first.", "Warning", 1);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Must select deleteable row first.", "Warning", 1);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


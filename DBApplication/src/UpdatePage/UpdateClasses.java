    public UpdateClasses(int class_ID) {
        classes_ID = class_ID;
        initComponents();
        EntityManagerFactory emf = javax.persistence.Persistence.createEntityManagerFactory("DBApplicationPU");
        EntityManager em = emf.createEntityManager();
        em.getTransaction().begin();
        classes = (Classes) em.createNamedQuery("Classes.findByClassNO").setParameter("classNO", classes_ID).getSingleResult();
        SimpleDateFormat format = new SimpleDateFormat("hh:mm:ss");
        txtClassID.setText(classes.getClassNO().toString());
        txtClassName.setText(classes.getClassName());
        txtBeginTime.setText(format.format(classes.getClassBeginTime()));
        txtEndTime.setText(format.format(classes.getClassEndTime()));
        em.getTransaction().commit();
    }


    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        if (txtBeginTime.getText().equals("") || txtClassID.getText().equals("") || txtClassName.getText().equals("") || txtEndTime.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Some field's are missing.", "Warning", 1);
        } else {
            try {
                EntityManagerFactory emf = javax.persistence.Persistence.createEntityManagerFactory("DBApplicationPU");
                EntityManager em = emf.createEntityManager();
                classes = em.find(Classes.class, classes_ID);
                em.getTransaction().begin();
                classes.setClassNO(Integer.parseInt(txtClassID.getText()));
                classes.setClassName(txtClassName.getText());
                SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy hh:mm:ss");
                String beginDate = "01/01/2000 " + txtBeginTime.getText();
                String endDate = "01/01/2000 " + txtEndTime.getText();
                classes.setClassBeginTime(format.parse(beginDate));
                classes.setClassEndTime(format.parse(endDate));
                em.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "Class is Updated.", "Warning", 1);
            } catch (Exception ex) {
                System.out.println(ex);
                JOptionPane.showMessageDialog(null, "Something happened badly please try again.", "Warning", 1);
            }
        }
    }//GEN-LAST:event_btnInsertActionPerformed

